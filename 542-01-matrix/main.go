func updateMatrix(mat [][]int) [][]int {
	m := len(mat)
	n := len(mat[0])

	res := make([][]int, m)
	for i := 0; i < m; i++ {
		res[i] = make([]int, n)
		for j := 0; j < n; j++ {
			res[i][j] = m + n // Initialize with maximum possible distance
		}
	}

	queue := make([][]int, 0, m*n)
	directions := [][]int{{-1, 0}, {1, 0}, {0, -1}, {0, 1}} // Possible adjacent directions

	// Add all cells with 0 value to the queue and mark them as visited
	for i := 0; i < m; i++ {
		for j := 0; j < n; j++ {
			if mat[i][j] == 0 {
				queue = append(queue, []int{i, j})
				res[i][j] = 0
			}
		}
	}

	for len(queue) > 0 {
		cell := queue[0]
		queue = queue[1:]
		x := cell[0]
		y := cell[1]

		for _, dir := range directions {
			newX := x + dir[0]
			newY := y + dir[1]

			if newX >= 0 && newX < m && newY >= 0 && newY < n && res[newX][newY] > res[x][y]+1 {
				res[newX][newY] = res[x][y] + 1
				queue = append(queue, []int{newX, newY})
			}
		}
	}

	return res
}

